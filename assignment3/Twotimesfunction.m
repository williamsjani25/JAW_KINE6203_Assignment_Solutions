function result=Twotimesfunction(input)
%This Twotimesfunction multiplies the input number by two.
%Input= the number you'd to multiply by 2
%Result= the answer
% For example: If input is "3", the result will be 6. 
%
%This solution also works for matrices, for the multiplication formatting
%is ".*". 
%For example: If the input is "[3 2 1]", the result is [6 4 2]

result=input.*2
end 