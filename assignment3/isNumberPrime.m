function result=isNumberPrime(input)
%The isNumberPrime function will return whether or not a number is a Prime
%number. As a reminder, a Prime number is a number that can only be
%divisible by 1 and itself.
%
%Input = The number you'd like to evaluate whether it is a prime number or
%not. 
%
%Output = The result
%
%If the input is a prime number, the result will return with 1 for True. If
%the input is not a prime number, the result will return with 0 for false. 
result = isprime(input)

end
